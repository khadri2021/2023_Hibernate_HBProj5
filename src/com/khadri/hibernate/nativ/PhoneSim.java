package com.khadri.hibernate.nativ;

public class PhoneSim {

	private int simSupport;

	private String phoneName;

	public int getSimSupport() {
		return simSupport;
	}

	public void setSimSupport(int simSupport) {
		this.simSupport = simSupport;
	}

	public String getPhoneName() {
		return phoneName;
	}

	public void setPhoneName(String phoneName) {
		this.phoneName = phoneName;
	}

}
